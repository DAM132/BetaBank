    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());

        int[] selectedRows = source.getSelectedRows();
        if (selectedRows.length == 0) {
            botonLiquidar1.setEnabled(false);
            botonModificar.setEnabled(false);
        } else {
            boolean rowSelected = false;
            for (int i = 0; i < selectedRows.length; i++) {
                rowSelected = selectedRows[i] == row;
                if (rowSelected) {
                    break;
                }
            }

            Integer idPrestamo = Integer.parseInt(source.getModel().getValueAt(row, 0) + "");
            idPrestamoCliente = idPrestamo;
            if (rowSelected && !prestamosSeleccionados.contains(idPrestamo)) {
                prestamosSeleccionados.add(idPrestamo);
            } else if (!rowSelected && prestamosSeleccionados.contains(idPrestamo)) {
                prestamosSeleccionados.remove(idPrestamo);
            }

            if (selectedRows.length == 1) {
                botonLiquidar1.setEnabled(true);
                botonModificar.setEnabled(true);
            } else if (selectedRows.length > 1) {
                botonLiquidar1.setEnabled(false);
                botonModificar.setEnabled(false);
            }
        }

    }//GEN-LAST:event_jTable1MouseClicked

