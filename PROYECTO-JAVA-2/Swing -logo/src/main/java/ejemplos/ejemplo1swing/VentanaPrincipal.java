    private boolean isValidoParaPrestamo(Usuario usuario) {
        boolean valido = false;
        Perfil perfil = usuario.getPerfil();
        if (!perfil.isMoroso()) {
            if (EstadoLaboral.ESTADOS_PAREJA.contains(perfil.getEstadolab())) {
                valido = comprobarCasados(usuario);
            } else if (EstadoLaboral.ESTADOS_ECONOMICOS.contains(perfil.getEstadolab()) && usuario.getMediaIngreso() > 1000) {
                valido = true;
            }
        }
        return valido;
    }

    private boolean comprobarCasados(Usuario usuario) {
        boolean valido = false;
        if (usuario.isCasado() && usuario.getIdPareja() != 0) {
            Usuario pareja = this.usuarioDAO.porId(usuario.getIdPareja());
            if (pareja.isActivo() && !EstadoLaboral.ESTADOS_PAREJA.contains(pareja.getPerfil().getEstadolab())) {
                valido = isValidoParaPrestamo(pareja);
            }
        }
        return valido;
    }

    private int calcularCantidadPreconcedidad(Usuario usuario) {
        int cantidadPrecondedida = 0;
        if (usuario.getMediaIngreso() < 2000) {
            cantidadPrecondedida = 5000;
        } else if (usuario.getMediaIngreso() < 3000) {
            cantidadPrecondedida = 10000;
        } else if (usuario.getMediaIngreso() > 3000) {
            cantidadPrecondedida = 15000;
        }
        return cantidadPrecondedida;
    }

    private boolean cantidadValida(Usuario usuario) {
        int cantidadPrecondedida = calcularCantidadPreconcedidad(usuario);
        int cantidadSolicitada = Integer.parseInt(introCantidadd.getText());
        boolean aprobado = cantidadPrecondedida >= cantidadSolicitada;

        return aprobado;
    }

    private double tipoInteresPorMeses(int meses) {
        double tipoInteres = 0;
        if (meses < 60) {
            tipoInteres = 0.05;
        } else if (meses < 120) {
            tipoInteres = 0.1;
        } else if (meses > 120) {
            tipoInteres = 0.15;
        }
        return tipoInteres;
    }

