    private void jTableMovMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMovMouseClicked
        // TODO add your handling code here:
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());

        int[] selectedRows = source.getSelectedRows();
        if (selectedRows.length == 0) {
            botonEliminar.setEnabled(false);
        } else {
            boolean rowSelected = false;
            for (int i = 0; i < selectedRows.length; i++) {
                rowSelected = selectedRows[i] == row;
                if (rowSelected) {
                    break;
                }
            }

            Integer idMovimiento = Integer.parseInt(source.getModel().getValueAt(row, 0) + "");
            idMovimientoCuenta = idMovimiento;
            if (rowSelected && !usuariosSeleccionados.contains(idMovimiento)) {
                usuariosSeleccionados.add(idMovimiento);
            } else if (!rowSelected && usuariosSeleccionados.contains(idMovimiento)) {
                usuariosSeleccionados.remove(idMovimiento);
            }

            if (selectedRows.length > 1) {
                jButton3.setEnabled(false);
                btnListarPrestamosClientes.setEnabled(true);
            } else if (selectedRows.length < 2) {
                btnListarPrestamosClientes.setEnabled(false);
                jButton3.setEnabled(true);
            }
        }

    }//GEN-LAST:event_jTableMovMouseClicked

