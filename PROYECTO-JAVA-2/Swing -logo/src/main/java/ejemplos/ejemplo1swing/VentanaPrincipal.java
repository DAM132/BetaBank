    private void llenarTablaMovimientos(int idusuario) {

        List<Movimiento> datos = new ArrayList<>();

        Usuario aux = usuarioDAO.porId(idusuario);
        Cuenta auxc = cuentaimp.porId(aux.getIdUsuario());
        List<MovCuenta> movimientos = movCuentaImp.listarPorCliente(auxc.getIban());

        try {

            String[] tituloColumnas = {"IDMOV", "Cantidad", "Concepto", "Emisor", "Destinatario"};

            for (MovCuenta movimiento : movimientos) {
                datos.add(mov.porId(movimiento.getIdMovCuenta()));
            }
            modeloTabla = new DefaultTableModel(null, tituloColumnas);
            this.jTableMov.setModel(modeloTabla);

            for (Movimiento movimiento : datos) {
                String[] datosUsuario = new String[tituloColumnas.length];
                int colum = 0;
                datosUsuario[colum++] = String.valueOf(movimiento.getIdMovimiento());
                datosUsuario[colum++] = String.valueOf(movimiento.getCantidad());
                datosUsuario[colum++] = movimiento.getConcepto();
                datosUsuario[colum++] = movimiento.getEmisor().toUpperCase();
                datosUsuario[colum++] = movimiento.getDestinatario().toUpperCase();
                modeloTabla.addRow(datosUsuario);
            }
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }

    }
    
