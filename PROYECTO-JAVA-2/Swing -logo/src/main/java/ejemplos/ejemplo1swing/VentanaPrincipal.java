    private void rellenarTablaUsuarios(List<Usuario> usuarios) {

        try {

            String[] tituloColumnas = {"ID", "DNI", "NOMBRE", "LOCALIDAD", "FECHA", "SEXO", "ACTIVO", "CASADO"};

            modeloTabla = new DefaultTableModel(null, tituloColumnas);
            tablaUsuarios.setModel(modeloTabla);

            for (Usuario usuario : usuarios) {
                String[] datosUsuario = new String[tituloColumnas.length];
                int colum = 0;
                datosUsuario[colum++] = String.valueOf(usuario.getIdUsuario());
                datosUsuario[colum++] = String.valueOf(usuario.getDni());
                datosUsuario[colum++] = String.valueOf(usuario.getNombre().toUpperCase());
                datosUsuario[colum++] = usuario.getLocalidad();
                datosUsuario[colum++] = String.valueOf(usuario.getFnac().getYear());
                datosUsuario[colum++] = String.valueOf(usuario.getSexo());
                if (usuario.isActivo()) {
                    datosUsuario[colum++] = "SI";
                } else if (!usuario.isActivo()) {
                    datosUsuario[colum++] = "NO";
                }
                if (usuario.isCasado()) {
                    datosUsuario[colum++] = "SI";
                } else if (!usuario.isCasado()) {
                    datosUsuario[colum++] = "NO";
                }
                modeloTabla.addRow(datosUsuario);
            }
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }

